<h1 style="color:blue;font-size:50px;">ADMINISTRATIVE OFFICE</h1>
<p style="color:darkpink;font-size:40px;">Welcome</p>

<p><b>Office of Admissions</b></p>

<p>The Admissions Office processes applications for admission to the school, identifies prospective applicants, and advises them about the admissions process, opportunities offered by departments and programs, and the school’s potential contribution to their professional development.</p>

<p><b>Office of Alumni Affairs and Career Advancement</b></p>

<p>The Office for Alumni Affairs and Career Advancement is a liaison between alumni and the faculty, students and administrators at the Harvard T.H. Chan School of Public Health. It develops and implements programs to facilitate alumni access to University resources, and serves as a networking forum for alumni, students, faculty and outside constituencies. Career advancement provides individual career counseling, job search resources, career fairs, recruiting events, networking opportunities, and career preparation programs to help students find challenging and rewarding work upon graduation.</p>

<p><b>Office of Communications</b></p>

<p>The Office of Communications serves as the primary information resource for the School. The office issues press releases announcing new research, helps connect journalists with faculty and researchers, produces the magazine Harvard Public Health and email newsletters The Kiosk, Update, and Nutrition Source Update, and creates content for the website. The Office can also answer questions about use of the School name, copyrights and other crediting.<p>

<p><b>Office of the Dean</b></p>

<p>Our central mission at Harvard T.H. Chan School of Public Health is to carry the banner of prevention to communities and nations. We wish to help the public understand the underlying reasons for poor health, and to implement prevention measures that will ensure that all citizens are born healthy and remain so throughout their lives.</p>

<p><b>Office of Diversity and Inclusion</b></p>

<p>The Office of Diversity and Inclusion supports new and existing initiatives that increase campus diversity among the Harvard Chan School community, with an emphasis on faculty, fellows, and students. The Office of Diversity and Inclusion will work to make the environment at the School more culturally competent and to have a positive impact on the School’s climate.</p>

<p><b>Office for External Relations</b></p>

<p>The Harvard T.H. Chan School of Public Health Office for External Relations is committed to securing the financial resources, time, and loyalty of alumni, friends, corporations, and foundations to promote the School’s mission of advancing discovery, learning, and communication in public health.</p>

<p><b>Office of Faculty Affairs</b></p>

<p>The Office of Faculty Affairs is responsible for all aspects of administration pertaining to faculty, research scientist, and annual appointments, as well as faculty searches and standing committees of the faculty.</p>

<p><b>Office of Financial Services & Administration</b></p>

<p>The Office of Financial Services & Administration provides oversight and assistance to Orgs in the areas of accounting and financial services, administrative applications development, budgets and financial planning and sponsored programs administration.</p>

<p><b>Human Resources</b></p>

<p>Human Resources supports the mission of the School through: employee and labor relations, performance management, compensation, employment, training and development, and legal compliance.</p>

<p><b>Office of Regulatory Affairs and Research Compliance (ORARC)</b></p>

<p><b>Previously Office of Human Research Administration</b></p>

<p>Administration is dedicated to educational, administrative, collaborative, and quality-assurance programs to ensure that human subjects are treated in our research community in a manner worthy of the public trust.</p>

<p><b>Information Technology Department</b></p>

<p>The Information Technology Department serves the School community in four key areas: Desktop, Media, Server/Network, and Web. Whether it’s desktop support through the helpdesk, streaming video from a class, email, remote file storage, or the school’s homepage, IT makes it happen.</p>

<p><b>Ombuds Office</b></p>

<p>The Ombuds office assists Harvard faculty, staff, students, and trainees who are experiencing conflict. The ombudsperson helps visitors organize their ideas, acquire relevant information, and create options for conflict resolution.</p>

<p><b>Operations Office</b></p>

<p>The Operations Department manages all day-to-day aspects of school facilities, security, mail services, and catering.</p>

<p><b>Registrar’s Office</b></p>

<p>The Registrar’s Office enrolls students, registers students for classes, maintains student data and grades, monitors students’ compliance with degree requirements, certifies enrollment, bills students’ tuition, and schedules courses.</p>

<p><b>Office for Student Affairs</b></p>

<p>The mission of the Office for Student Affairs is to enrich the student experience at the Harvard T.H. Chan School of Public Health through a wide range of educational, cultural, and social programming.</p>

<p><b>Office of Student Information Systems</b></p>

<p>The Office of Student Information Systems (SIS) provides technical support for processing admissions, course, schedule, registration, records, enrollment verification and account receivables data for all student, faculty and staff.</p>

<p><b>Student Financial Services</b></p>

<p>The role of Student Financial Services is to assist students and applicants with financing their education at the Harvard T.H. Chan School of Public Health.</p>

<p><b>Office for Student Services</b></p>

<p>The mission of the Office for Student Services is to support all activities related to students’ admission, registration, progress to degree, and financial support at the Harvard T.H. Chan School of Public Health.</p>
